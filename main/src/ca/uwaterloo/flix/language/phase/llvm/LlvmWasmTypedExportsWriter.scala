  private def renderTranslateRuntimeExec(resultTpe: AbiType, execVar: String): String = {
    val sb = new StringBuilder(1024)
    sb.append(s"  switch ($execVar.tag) {\n")
    sb.append("    case FLIX_RUNTIME_RUNTIME_EXEC_OK:\n")
