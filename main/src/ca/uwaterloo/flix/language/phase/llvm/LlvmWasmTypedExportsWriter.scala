/**
  * Renders the typed wasm export adapter layered on top of the internal `flix:runtime/runtime` substrate.
  *
  * Public design:
  *   - exported defs are surfaced as typed methods on `api.ctx`,
  *   - suspensions and generic values are public resources owned by the `api` interface,
  *   - resumption uses per-export `resume-*` methods, matching the native export ABI more closely,
  *   - the low-level runtime scheduler/resource substrate stays internal to the adapter component.
  */
object LlvmWasmTypedExportsWriter {

  case class Entry(witName: String,
                   symbol: String,
                   defId: Long,
                   signature: ExportAbi.Signature,
                   resumeType: Option[AbiType],
                   requestSignature: Option[ExportAbi.Signature])

  def typedComponentPath(outputPath: java.nio.file.Path, artifactName: String = ArtifactNames.DefaultBaseName): java.nio.file.Path =
    LlvmWasmDriver.wasmDirPath(outputPath).resolve(ArtifactNames.wasmTypedExportComponentFileName(artifactName))

  def typedWitDirPath(outputPath: java.nio.file.Path, artifactName: String = ArtifactNames.DefaultBaseName): java.nio.file.Path =
    LlvmWasmDriver.wasmDirPath(outputPath).resolve(ArtifactNames.wasmTypedExportWitDirName(artifactName))

  def typedComponentJsPath(outputPath: java.nio.file.Path, artifactName: String = ArtifactNames.DefaultBaseName): java.nio.file.Path =
    LlvmWasmDriver.jsOutDirPath(outputPath).resolve(ArtifactNames.wasmTypedExportComponentJsFileName(artifactName)).toAbsolutePath.normalize()

  def typedComponentTypesPath(outputPath: java.nio.file.Path, artifactName: String = ArtifactNames.DefaultBaseName): java.nio.file.Path =
    LlvmWasmDriver.jsOutDirPath(outputPath).resolve(ArtifactNames.wasmTypedExportComponentTypesFileName(artifactName)).toAbsolutePath.normalize()

  def compute(root: LoweredAst.Root): List[Entry] = {
    val exports = LlvmWasmDefs.compute(root).filter(_.isExport)
    if (exports.isEmpty) return Nil

    val suspensionSummaries = LlvmExportSuspensionAnalysis.compute(root)
    val used = mutable.HashSet.empty[String]

    exports.map { e =>
      val base = baseWitName(e.sym.toString)
      val witName = uniqueWitName(base, e.defId, used)
      Entry(
        witName = witName,
        symbol = e.sym.toString,
        defId = e.defId,
        signature = e.signature,
        resumeType = LlvmExportSuspensionAnalysis.typedResumeType(e.sym, suspensionSummaries, root),
        requestSignature = LlvmExportSuspensionAnalysis.typedRequestSignature(e.sym, suspensionSummaries, root)
      )
    }
  }

  def renderPublicBindings(entries: List[Entry], importedInterfaces: List[String] = Nil): String = {
    val sb = new StringBuilder(8 * 1024)
    sb.append("package flix:exports@0.1.0;\n\n")
    sb.append(renderApiInterface(entries))
    sb.append("\n")
    sb.append("world flix {\n")
    sb.append("  import flix:sys/sys@0.1.0;\n")
    importedInterfaces.distinct.sorted.foreach { iface =>
      sb.append(s"  import $iface;\n")
    }
    sb.append("  export api;\n")
    sb.append("}\n")
    sb.toString()
  }

