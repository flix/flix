  private def renderApiInterface(entries: List[Entry]): String = {
    val sb = new StringBuilder(8 * 1024)
    sb.append("interface api {\n")
    sb.append("  use flix:runtime/runtime@0.1.0.{suspension-info};\n\n")
    sb.append("  resource value;\n")
    sb.append("  resource suspension;\n\n")

    aggregateTypes(entries).foreach { tpe =>
      sb.append(renderAggregateTypeDef(tpe))
      sb.append("\n")
    }

    entries.foreach { e =>
      e.requestSignature.foreach { sig =>
        sb.append(renderRequestTypeDef(e, sig))
        sb.append("\n")
      }
    }

    resultTypes(entries).foreach { tpe =>
      sb.append(s"  variant ${resultVariantName(tpe)} {\n")
      tpe match {
        case AbiType.Unit => sb.append("    ok,\n")
        case _ => sb.append(s"    ok(${witTypeOf(tpe)}),\n")
      }
      sb.append("    thrown(value),\n")
      sb.append("    suspended(suspension),\n")
      sb.append("  }\n\n")
    }

    sb.append("  resource ctx {\n")
    sb.append("    constructor();\n")
    runtimeBoxMethodSpecs.foreach { case (name, tpe) =>
      sb.append(s"    $name: func(x: ${witTypeOf(tpe)}) -> value;\n")
    }
    runtimeUnboxMethodSpecs.foreach { case (name, tpe) =>
      sb.append(s"    $name: func(v: borrow<value>) -> ${witTypeOf(tpe)};\n")
    }
    sb.append("    suspension-peek: func(s: borrow<suspension>) -> suspension-info;\n")
    sb.append("    suspension-arg-count: func(s: borrow<suspension>) -> u32;\n")
    sb.append("    suspension-arg-as-i64: func(s: borrow<suspension>, idx: u32) -> value;\n")
    sb.append("    suspension-arg-as-ptr: func(s: borrow<suspension>, idx: u32) -> value;\n")
    entries.foreach { e =>
      e.requestSignature.foreach { _ =>
        sb.append(s"    request-${e.witName}: func(s: borrow<suspension>) -> ${requestTypeName(e)};\n")
      }
    }

    entries.foreach { e =>
      val params = e.signature.params.zipWithIndex.map {
        case (tpe, idx) => s"a$idx: ${witTypeOf(tpe)}"
      }.mkString(", ")
      sb.append(s"    ${e.witName}: func(${params}) -> ${resultVariantName(e.signature.result)};\n")
      val resumeParamType = e.resumeType match {
        case Some(tpe) => witTypeOf(tpe)
        case None => "borrow<value>"
      }
      sb.append(s"    resume-${e.witName}: func(s: suspension, resume: $resumeParamType) -> ${resultVariantName(e.signature.result)};\n")
    }

    sb.append("  }\n")
    sb.append("}\n")
    sb.toString()
  }

  private def renderBoxMethod(name: String, tpe: AbiType): String = {
    val cFuncName = s"exports_flix_exports_api_method_ctx_${cIdent(name)}"
    val paramType = adapterParamCType(tpe)
    val runtimeCall = runtimeBoxMethodName(tpe)
    s"""exports_flix_exports_api_own_value_t $cFuncName(exports_flix_exports_api_borrow_ctx_t self, $paramType x) {
       |  flix_runtime_runtime_borrow_ctx_t ctx = flix_runtime_runtime_borrow_ctx(self->runtime_ctx);
       |  return flix_api_wrap_value($runtimeCall(ctx, x));
       |}
       |""".stripMargin
  }

  private def renderUnboxMethod(name: String, tpe: AbiType): String = {
    val cFuncName = s"exports_flix_exports_api_method_ctx_${cIdent(name)}"
    val runtimeCall = runtimeUnboxMethodName(tpe)
    tpe match {
      case AbiType.String | AbiType.Bytes =>
        val resultType = if (tpe == AbiType.String) "adapter_string_t" else "adapter_list_u8_t"
        s"""void $cFuncName(exports_flix_exports_api_borrow_ctx_t self, exports_flix_exports_api_borrow_value_t v, $resultType *ret) {
           |  flix_runtime_runtime_borrow_ctx_t ctx = flix_runtime_runtime_borrow_ctx(self->runtime_ctx);
           |  $runtimeCall(ctx, flix_api_borrow_value(v), ret);
           |}
           |""".stripMargin

      case _ =>
        val resultType = adapterParamCType(tpe)
        s"""$resultType $cFuncName(exports_flix_exports_api_borrow_ctx_t self, exports_flix_exports_api_borrow_value_t v) {
           |  flix_runtime_runtime_borrow_ctx_t ctx = flix_runtime_runtime_borrow_ctx(self->runtime_ctx);
           |  return $runtimeCall(ctx, flix_api_borrow_value(v));
           |}
           |""".stripMargin
    }
  }

