import * as net from "node:net";

const KIND = {
  AlreadyExists: 0,
  ConnectionFailed: 1,
  Interrupted: 2,
  InvalidPath: 3,
  InvalidInput: 4,
  InvalidData: 5,
  NotFound: 6,
  NotFile: 7,
  NotDirectory: 8,
  PermissionDenied: 9,
  Timeout: 10,
  UnexpectedEof: 11,
  Unsupported: 12,
  UnknownHost: 13,
  Other: 14,
};

function tcpIoErrorFromNodeError(err) {
  if (!err || typeof err !== "object") return { kindCode: KIND.Other, msg: String(err) };

  const code = typeof err.code === "string" ? err.code : "";
  const msg = typeof err.message === "string" ? err.message : String(err);

  switch (code) {
    case "ECONNREFUSED":
    case "ECONNRESET":
    case "ENETUNREACH":
    case "EHOSTUNREACH":
      return { kindCode: KIND.ConnectionFailed, msg };
    case "ETIMEDOUT":
      return { kindCode: KIND.Timeout, msg };
    case "EADDRINUSE":
      return { kindCode: KIND.AlreadyExists, msg };
    case "EACCES":
    case "EPERM":
      return { kindCode: KIND.PermissionDenied, msg };
    case "EINVAL":
    case "EADDRNOTAVAIL":
      return { kindCode: KIND.InvalidInput, msg };
    case "ENOTFOUND":
      return { kindCode: KIND.UnknownHost, msg };
    default:
      return { kindCode: KIND.Other, msg };
  }
}

function ipBytesToString(ipBytes) {
  const bytes = Array.from(ipBytes ?? []);

  if (bytes.length === 4) {
    return bytes.join(".");
  }

  if (bytes.length === 16) {
    const parts = [];
    for (let i = 0; i < 16; i += 2) {
      const hi = bytes[i] & 0xff;
      const lo = bytes[i + 1] & 0xff;
      parts.push(((hi << 8) | lo).toString(16));
    }
    // Uncompressed IPv6; Node accepts it and it's fine for our purposes.
    return parts.join(":");
  }

  throw new Error(`invalid IP byte length: ${bytes.length} (expected 4 or 16)`);
}

function ensureU16(x) {
  if (!Number.isInteger(x) || x < 0 || x > 65535) {
    throw new Error(`invalid u16: ${x}`);
  }
  return x;
}

function ensureU32(x) {
  if (!Number.isInteger(x) || x < 0 || x > 0xffffffff) {
    throw new Error(`invalid u32: ${x}`);
  }
  return x;
}

function takeBytes(buffers, maxBytes) {
  if (maxBytes <= 0) return Buffer.alloc(0);
  if (buffers.length === 0) return null;

  let remaining = maxBytes;
  const parts = [];

  while (remaining > 0 && buffers.length > 0) {
    const b0 = buffers[0];
    if (b0.length <= remaining) {
      parts.push(b0);
      buffers.shift();
      remaining -= b0.length;
    } else {
      parts.push(b0.subarray(0, remaining));
      buffers[0] = b0.subarray(remaining);
      remaining = 0;
    }
  }

  return Buffer.concat(parts);
}

function bufferToU8(buf) {
  return new Uint8Array(buf.buffer, buf.byteOffset, buf.byteLength);
}

function makeIdGenerator(start = 1n) {
  let next = start;
  return () => {
    const out = next;
    next += 1n;
    return out;
  };
}

/**
 * Returns a set of Node.js-backed handlers for the `tcp-*` ops in `flix:runtime@0.1.0`.
 *
 * Notes:
 * - Socket/server ids are stable `u64` (JS BigInt) generated by the host.
 * - Reads complete when *any* data is available (up to `max-bytes`), or with empty bytes on EOF.
 */
export function makeNodeTcpHandlers(options = {}) {
  const connectTimeoutMs = options.connectTimeoutMs ?? null; // null => no timeout

  const newSocketId = makeIdGenerator(1000n);
  const newServerId = makeIdGenerator(5000n);

  /** @type {Map<bigint, any>} */
  const sockets = new Map();
  /** @type {Map<bigint, any>} */
  const servers = new Map();

  function socketErr(socketId, msg) {
    return { kindCode: KIND.InvalidInput, msg: `invalid TCP socket handle: ${socketId} (${msg})` };
  }

  function serverErr(serverId, msg) {
    return { kindCode: KIND.InvalidInput, msg: `invalid TCP server handle: ${serverId} (${msg})` };
  }

  function registerSocket(sock) {
    const socketId = newSocketId();

    const state = {
      socketId,
      socket: sock,
      ended: false,
      closed: false,
      error: null,
      buffers: [],
      reads: [],
    };

    const flushOneRead = () => {
      if (state.reads.length === 0) return;

      const pending = state.reads.shift();
      const chunk = takeBytes(state.buffers, pending.maxBytes) ?? Buffer.alloc(0);
      pending.resolve(chunk);
    };

    sock.on("data", (chunk) => {
      state.buffers.push(chunk);
      flushOneRead();
    });

    sock.on("end", () => {
      state.ended = true;
      while (state.reads.length > 0) {
        flushOneRead();
      }
    });

    sock.on("error", (err) => {
      state.error = err;
      while (state.reads.length > 0) {
        const pending = state.reads.shift();
        pending.reject(err);
      }
    });

    sock.on("close", () => {
      state.closed = true;
      if (!state.ended && state.error == null) {
        state.ended = true;
        while (state.reads.length > 0) {
          flushOneRead();
        }
      }
    });

    sockets.set(socketId, state);
    return socketId;
  }

  async function connectTcp(ip, port) {
    const host = ipBytesToString(ip);
    const p = ensureU16(port);

    const sock = net.createConnection({ host, port: p });

    if (connectTimeoutMs != null) {
      sock.setTimeout(connectTimeoutMs, () => {
        // Trigger an error path.
        sock.destroy(Object.assign(new Error("connect timeout"), { code: "ETIMEDOUT" }));
      });
    }

    await new Promise((resolve, reject) => {
      const onConnect = () => {
        cleanup();
        resolve();
      };
      const onError = (err) => {
        cleanup();
        reject(err);
      };
      const cleanup = () => {
        sock.off("connect", onConnect);
        sock.off("error", onError);
      };
      sock.on("connect", onConnect);
      sock.on("error", onError);
    });

    // Clear any configured connect timeout after success.
    if (connectTimeoutMs != null) {
      sock.setTimeout(0);
    }

    return sock;
  }

  async function readTcp(socketId, maxBytes) {
    const state = sockets.get(socketId);
    if (!state) throw Object.assign(new Error(`no socket ${socketId}`), { flixKindCode: KIND.InvalidInput });
    if (state.error) throw state.error;

    const n = ensureU32(maxBytes);
    const immediate = takeBytes(state.buffers, n);
    if (immediate && immediate.length > 0) return immediate;
    if (state.ended) return Buffer.alloc(0);

    return await new Promise((resolve, reject) => {
      state.reads.push({ maxBytes: n, resolve, reject });
    });
  }

  async function writeTcp(socketId, bytes) {
    const state = sockets.get(socketId);
    if (!state) throw Object.assign(new Error(`no socket ${socketId}`), { flixKindCode: KIND.InvalidInput });
    if (state.error) throw state.error;

    const buf = Buffer.from(bytes ?? []);
    if (buf.length === 0) return;

    await new Promise((resolve, reject) => {
      const sock = state.socket;

      const onError = (err) => {
        cleanup();
        reject(err);
      };

      const cleanup = () => {
        sock.off("error", onError);
      };

      sock.once("error", onError);
      sock.write(buf, () => {
        cleanup();
        resolve();
      });
    });
  }

  async function closeTcp(socketId) {
    const state = sockets.get(socketId);
    if (!state) return;

    const sock = state.socket;
    if (sock.destroyed) {
      sockets.delete(socketId);
      return;
    }

    await new Promise((resolve) => {
      sock.once("close", resolve);
      // `end` attempts a graceful half-close; also `destroy` to ensure it doesn't hang.
      sock.end();
      sock.destroy();
    });

    sockets.delete(socketId);
  }

  function registerServer(server) {
    const serverId = newServerId();

    const state = {
      serverId,
      server,
      accepts: [],
      pending: [],
    };

    server.on("connection", (sock) => {
      if (state.pending.length > 0) {
        const p = state.pending.shift();
        p.resolve(sock);
      } else {
        state.accepts.push(sock);
      }
    });

    server.on("close", () => {
      servers.delete(serverId);
    });

    servers.set(serverId, state);
    return serverId;
  }

  async function bindTcpServer(ip, port) {
    const host = ipBytesToString(ip);
    const p = ensureU16(port);

    const server = net.createServer({ allowHalfOpen: false });

    await new Promise((resolve, reject) => {
      const onListening = () => {
        cleanup();
        resolve();
      };
      const onError = (err) => {
        cleanup();
        reject(err);
      };
      const cleanup = () => {
        server.off("listening", onListening);
        server.off("error", onError);
      };
      server.on("listening", onListening);
      server.on("error", onError);
      server.listen({ host, port: p });
    });

    return server;
  }

  async function acceptTcpServer(serverId) {
    const st = servers.get(serverId);
    if (!st) throw Object.assign(new Error(`no server ${serverId}`), { flixKindCode: KIND.InvalidInput });

    if (st.accepts.length > 0) return st.accepts.shift();

    return await new Promise((resolve, reject) => {
      st.pending.push({ resolve, reject });
    });
  }

  function localPort(serverId) {
    const st = servers.get(serverId);
    if (!st) throw Object.assign(new Error(`no server ${serverId}`), { flixKindCode: KIND.InvalidInput });

    const addr = st.server.address();
    if (!addr || typeof addr !== "object") {
      throw Object.assign(new Error("server not listening"), { flixKindCode: KIND.Other });
    }
    return addr.port;
  }

  async function closeTcpServer(serverId) {
    const st = servers.get(serverId);
    if (!st) return;

    const server = st.server;
    await new Promise((resolve) => server.close(resolve));
  }

  return {
    "tcp-socket-connect": async ({ runtime, ctx, suspension, request }) => {
      try {
        const sock = await connectTcp(request.ip, request.port);
        const socketId = registerSocket(sock);
        runtime.resumeTcpSocketConnectOk(ctx, suspension, socketId);
      } catch (e) {
        runtime.resumeTcpSocketConnectErr(ctx, suspension, tcpIoErrorFromNodeError(e));
      }
    },

    "tcp-socket-read": async ({ runtime, ctx, suspension, request }) => {
      const socketId = request.socketId;
      try {
        const buf = await readTcp(socketId, request.maxBytes);
        runtime.resumeTcpSocketReadOk(ctx, suspension, bufferToU8(buf));
      } catch (e) {
        const code = typeof e?.flixKindCode === "number" ? e.flixKindCode : null;
        runtime.resumeTcpSocketReadErr(ctx, suspension, code != null ? socketErr(socketId, e.message) : tcpIoErrorFromNodeError(e));
      }
    },

    "tcp-socket-write": async ({ runtime, ctx, suspension, request }) => {
      const socketId = request.socketId;
      try {
        await writeTcp(socketId, request.bytes);
        runtime.resumeTcpSocketWriteOk(ctx, suspension);
      } catch (e) {
        const code = typeof e?.flixKindCode === "number" ? e.flixKindCode : null;
        runtime.resumeTcpSocketWriteErr(ctx, suspension, code != null ? socketErr(socketId, e.message) : tcpIoErrorFromNodeError(e));
      }
    },

    "tcp-socket-close": async ({ runtime, ctx, suspension, request }) => {
      const socketId = request.socketId;
      try {
        await closeTcp(socketId);
        runtime.resumeTcpSocketCloseOk(ctx, suspension);
      } catch (e) {
        runtime.resumeTcpSocketCloseErr(ctx, suspension, tcpIoErrorFromNodeError(e));
      }
    },

    "tcp-server-bind": async ({ runtime, ctx, suspension, request }) => {
      try {
        const server = await bindTcpServer(request.ip, request.port);
        const serverId = registerServer(server);
        runtime.resumeTcpServerBindOk(ctx, suspension, serverId);
      } catch (e) {
        runtime.resumeTcpServerBindErr(ctx, suspension, tcpIoErrorFromNodeError(e));
      }
    },

    "tcp-server-accept": async ({ runtime, ctx, suspension, request }) => {
      const serverId = request.serverId;
      try {
        const sock = await acceptTcpServer(serverId);
        const socketId = registerSocket(sock);
        runtime.resumeTcpServerAcceptOk(ctx, suspension, socketId);
      } catch (e) {
        const code = typeof e?.flixKindCode === "number" ? e.flixKindCode : null;
        runtime.resumeTcpServerAcceptErr(ctx, suspension, code != null ? serverErr(serverId, e.message) : tcpIoErrorFromNodeError(e));
      }
    },

    "tcp-server-local-port": async ({ runtime, ctx, suspension, request }) => {
      const serverId = request.serverId;
      try {
        const port = localPort(serverId);
        runtime.resumeTcpServerLocalPortOk(ctx, suspension, ensureU16(port));
      } catch (e) {
        const code = typeof e?.flixKindCode === "number" ? e.flixKindCode : null;
        runtime.resumeTcpServerLocalPortErr(ctx, suspension, code != null ? serverErr(serverId, e.message) : tcpIoErrorFromNodeError(e));
      }
    },

    "tcp-server-close": async ({ runtime, ctx, suspension, request }) => {
      const serverId = request.serverId;
      try {
        await closeTcpServer(serverId);
        runtime.resumeTcpServerCloseOk(ctx, suspension);
      } catch (e) {
        runtime.resumeTcpServerCloseErr(ctx, suspension, tcpIoErrorFromNodeError(e));
      }
    },
  };
}
