import { spawn } from "node:child_process";

const KIND = {
  AlreadyExists: 0,
  ConnectionFailed: 1,
  Interrupted: 2,
  InvalidPath: 3,
  InvalidInput: 4,
  InvalidData: 5,
  NotFound: 6,
  NotFile: 7,
  NotDirectory: 8,
  PermissionDenied: 9,
  Timeout: 10,
  UnexpectedEof: 11,
  Unsupported: 12,
  UnknownHost: 13,
  Other: 14,
};

function bigintToSafeNumber(x) {
  if (typeof x !== "bigint") return Number(x);
  const max = BigInt(Number.MAX_SAFE_INTEGER);
  if (x > max) return Number.MAX_SAFE_INTEGER;
  if (x < -max) return -Number.MAX_SAFE_INTEGER;
  return Number(x);
}

function clampMs(ms) {
  if (!Number.isFinite(ms) || ms <= 0) return 0;
  return Math.min(ms, 0x7fffffff);
}

function ensureU32(x) {
  if (!Number.isInteger(x) || x < 0 || x > 0xffffffff) {
    throw new Error(`invalid u32: ${x}`);
  }
  return x;
}

function takeBytes(buffers, maxBytes) {
  if (maxBytes <= 0) return Buffer.alloc(0);
  if (buffers.length === 0) return null;

  let remaining = maxBytes;
  const parts = [];

  while (remaining > 0 && buffers.length > 0) {
    const b0 = buffers[0];
    if (b0.length <= remaining) {
      parts.push(b0);
      buffers.shift();
      remaining -= b0.length;
    } else {
      parts.push(b0.subarray(0, remaining));
      buffers[0] = b0.subarray(remaining);
      remaining = 0;
    }
  }

  return Buffer.concat(parts);
}

function bufferToU8(buf) {
  return new Uint8Array(buf.buffer, buf.byteOffset, buf.byteLength);
}

function makeIdGenerator(start = 1n) {
  let next = start;
  return () => {
    const out = next;
    next += 1n;
    return out;
  };
}

function processIoErrorFromNodeError(err) {
  if (!err || typeof err !== "object") return { kindCode: KIND.Other, msg: String(err) };

  const code = typeof err.code === "string" ? err.code : "";
  const msg = typeof err.message === "string" ? err.message : String(err);

  // Preserve JVM-ish behavior: most process failures surface as `Other`.
  switch (code) {
    case "EINVAL":
    case "ERR_INVALID_ARG_TYPE":
    case "ERR_INVALID_ARG_VALUE":
      return { kindCode: KIND.InvalidInput, msg };
    default:
      return { kindCode: KIND.Other, msg };
  }
}

function exitCodeFromSignal(signal) {
  // Best-effort mapping: many runtimes use 128 + signal number.
  // This aligns reasonably well with common Unix conventions and JVM `Process` behavior.
  const SIGNAL_NUM = {
    SIGINT: 2,
    SIGQUIT: 3,
    SIGABRT: 6,
    SIGKILL: 9,
    SIGALRM: 14,
    SIGTERM: 15,
    SIGPIPE: 13,
    SIGUSR1: 10,
    SIGUSR2: 12,
  };

  const n = SIGNAL_NUM[signal];
  if (typeof n === "number") return 128 + n;
  return 128;
}

function normalizeExitCode(code, signal) {
  if (typeof code === "number" && Number.isInteger(code)) return code | 0;
  if (typeof signal === "string" && signal.length > 0) return exitCodeFromSignal(signal);
  return 0;
}

async function awaitSpawn(child) {
  await new Promise((resolve, reject) => {
    const onSpawn = () => {
      cleanup();
      resolve();
    };
    const onError = (err) => {
      cleanup();
      reject(err);
    };
    const cleanup = () => {
      child.off("spawn", onSpawn);
      child.off("error", onError);
    };
    child.once("spawn", onSpawn);
    child.once("error", onError);
  });
}

/**
 * Returns a set of Node.js-backed handlers for the `process-*` ops in `flix:runtime@0.1.0`.
 *
 * Notes:
 * - Process ids are stable `u64` (JS BigInt) generated by the host.
 * - stdout/stderr reads return up to `max-bytes`; empty bytes denotes EOF.
 * - `process-release` forgets the handle without killing the process (matches JVM behavior).
 */
export function makeNodeProcessHandlers(options = {}) {
  const newProcessId = makeIdGenerator(9000n);

  /** @type {Map<bigint, any>} */
  const processes = new Map();

  function invalidHandle() {
    return { kindCode: KIND.Other, msg: "invalid process handle." };
  }

  function registerProcess(child) {
    const processId = newProcessId();

    const state = {
      processId,
      child,
      exited: false,
      exitCode: null,
      exitSignal: null,
      exitPromise: null,
      stdoutEnded: false,
      stdoutError: null,
      stdoutBuffers: [],
      stdoutReads: [],
      stderrEnded: false,
      stderrError: null,
      stderrBuffers: [],
      stderrReads: [],
      // listener refs for cleanup
      _onExit: null,
      _onStdoutData: null,
      _onStdoutEnd: null,
      _onStdoutError: null,
      _onStderrData: null,
      _onStderrEnd: null,
      _onStderrError: null,
    };

    state.exitPromise = new Promise((resolve) => {
      state._onExit = (code, signal) => {
        state.exited = true;
        state.exitCode = code;
        state.exitSignal = signal;
        resolve({ code, signal });

        // Unblock pending reads with EOF if streams have ended.
        if (state.stdoutEnded) {
          while (state.stdoutReads.length > 0) {
            const p = state.stdoutReads.shift();
            p.resolve(Buffer.alloc(0));
          }
        }
        if (state.stderrEnded) {
          while (state.stderrReads.length > 0) {
            const p = state.stderrReads.shift();
            p.resolve(Buffer.alloc(0));
          }
        }
      };
      child.once("exit", state._onExit);
    });

    const stdout = child.stdout;
    if (stdout) {
      const flush = () => {
        if (state.stdoutReads.length === 0) return;
        const pending = state.stdoutReads.shift();
        const chunk = takeBytes(state.stdoutBuffers, pending.maxBytes) ?? Buffer.alloc(0);
        pending.resolve(chunk);
      };

      state._onStdoutData = (chunk) => {
        state.stdoutBuffers.push(chunk);
        flush();
      };
      state._onStdoutEnd = () => {
        state.stdoutEnded = true;
        while (state.stdoutReads.length > 0) flush();
      };
      state._onStdoutError = (err) => {
        state.stdoutError = err;
        while (state.stdoutReads.length > 0) state.stdoutReads.shift().reject(err);
      };

      stdout.on("data", state._onStdoutData);
      stdout.on("end", state._onStdoutEnd);
      stdout.on("error", state._onStdoutError);
    } else {
      state.stdoutEnded = true;
    }

    const stderr = child.stderr;
    if (stderr) {
      const flush = () => {
        if (state.stderrReads.length === 0) return;
        const pending = state.stderrReads.shift();
        const chunk = takeBytes(state.stderrBuffers, pending.maxBytes) ?? Buffer.alloc(0);
        pending.resolve(chunk);
      };

      state._onStderrData = (chunk) => {
        state.stderrBuffers.push(chunk);
        flush();
      };
      state._onStderrEnd = () => {
        state.stderrEnded = true;
        while (state.stderrReads.length > 0) flush();
      };
      state._onStderrError = (err) => {
        state.stderrError = err;
        while (state.stderrReads.length > 0) state.stderrReads.shift().reject(err);
      };

      stderr.on("data", state._onStderrData);
      stderr.on("end", state._onStderrEnd);
      stderr.on("error", state._onStderrError);
    } else {
      state.stderrEnded = true;
    }

    processes.set(processId, state);
    return processId;
  }

  function unregisterProcess(processId) {
    const state = processes.get(processId);
    if (!state) return;
    processes.delete(processId);

    const child = state.child;
    try {
      if (state._onExit) child.off("exit", state._onExit);
    } catch {
      // ignore
    }

    const stdout = child.stdout;
    if (stdout) {
      if (state._onStdoutData) stdout.off("data", state._onStdoutData);
      if (state._onStdoutEnd) stdout.off("end", state._onStdoutEnd);
      if (state._onStdoutError) stdout.off("error", state._onStdoutError);
    }

    const stderr = child.stderr;
    if (stderr) {
      if (state._onStderrData) stderr.off("data", state._onStderrData);
      if (state._onStderrEnd) stderr.off("end", state._onStderrEnd);
      if (state._onStderrError) stderr.off("error", state._onStderrError);
    }
  }

  async function readStream(kind, state, maxBytes) {
    const n = ensureU32(maxBytes);

    const endedKey = kind === "stdout" ? "stdoutEnded" : "stderrEnded";
    const errorKey = kind === "stdout" ? "stdoutError" : "stderrError";
    const buffersKey = kind === "stdout" ? "stdoutBuffers" : "stderrBuffers";
    const readsKey = kind === "stdout" ? "stdoutReads" : "stderrReads";

    if (state[errorKey]) throw state[errorKey];

    const immediate = takeBytes(state[buffersKey], n);
    if (immediate && immediate.length > 0) return immediate;
    if (state[endedKey]) return Buffer.alloc(0);

    return await new Promise((resolve, reject) => {
      state[readsKey].push({ maxBytes: n, resolve, reject });
    });
  }

  async function writeStdin(state, bytes) {
    const stdin = state.child.stdin;
    if (!stdin) throw new Error("stdin unavailable");

    const buf = Buffer.from(bytes ?? []);
    if (buf.length === 0) return;

    await new Promise((resolve, reject) => {
      const onError = (err) => {
        cleanup();
        reject(err);
      };
      const cleanup = () => {
        stdin.off("error", onError);
      };
      stdin.once("error", onError);
      stdin.write(buf, () => {
        cleanup();
        resolve();
      });
    });
  }

  function buildEnv(envOverrides) {
    const base = Object.assign({}, process.env);
    for (const entry of envOverrides ?? []) {
      if (!entry || typeof entry !== "object") continue;
      if (typeof entry.key !== "string") continue;
      if (typeof entry.value !== "string") continue;
      base[entry.key] = entry.value;
    }
    return base;
  }

  return {
    "process-exec": async ({ runtime, ctx, suspension, request }) => {
      const argv = Array.isArray(request.argv) ? request.argv : [];
      if (argv.length === 0 || typeof argv[0] !== "string" || argv[0].length === 0) {
        runtime.resumeProcessExecErr(ctx, suspension, {
          kindCode: KIND.InvalidInput,
          msg: "argv must be a non-empty list",
        });
        return;
      }

      const cmd = argv[0];
      const args = argv.slice(1);

      const cwd = request.cwd ?? null;
      if (cwd != null && typeof cwd !== "string") {
        runtime.resumeProcessExecErr(ctx, suspension, {
          kindCode: KIND.InvalidInput,
          msg: "cwd must be a string",
        });
        return;
      }

      const env = buildEnv(request.env);

      let child;
      try {
        child = spawn(cmd, args, {
          cwd: cwd ?? undefined,
          env,
          stdio: ["pipe", "pipe", "pipe"],
          windowsHide: true,
        });
      } catch (e) {
        runtime.resumeProcessExecErr(ctx, suspension, processIoErrorFromNodeError(e));
        return;
      }

      // Register immediately so we never miss early exit events.
      const processId = registerProcess(child);

      try {
        await awaitSpawn(child);
        runtime.resumeProcessExecOk(ctx, suspension, processId);
      } catch (e) {
        unregisterProcess(processId);
        runtime.resumeProcessExecErr(ctx, suspension, processIoErrorFromNodeError(e));
      }
    },

    "process-exit-value": async ({ runtime, ctx, suspension, request }) => {
      const processId = request.processId;
      const state = processes.get(processId);
      if (!state) {
        runtime.resumeProcessExitValueErr(ctx, suspension, invalidHandle());
        return;
      }

      if (!state.exited) {
        runtime.resumeProcessExitValueErr(ctx, suspension, {
          kindCode: KIND.Other,
          msg: "process has not exited",
        });
        return;
      }

      const code = normalizeExitCode(state.exitCode, state.exitSignal);
      runtime.resumeProcessExitValueOk(ctx, suspension, code);
    },

    "process-is-alive": async ({ runtime, ctx, suspension, request }) => {
      const processId = request.processId;
      const state = processes.get(processId);
      if (!state) {
        runtime.resumeProcessIsAliveErr(ctx, suspension, invalidHandle());
        return;
      }

      runtime.resumeProcessIsAliveOk(ctx, suspension, !state.exited);
    },

    "process-pid": async ({ runtime, ctx, suspension, request }) => {
      const processId = request.processId;
      const state = processes.get(processId);
      if (!state) {
        runtime.resumeProcessPidErr(ctx, suspension, invalidHandle());
        return;
      }

      const pid = state.child.pid;
      if (!Number.isInteger(pid) || pid <= 0) {
        runtime.resumeProcessPidErr(ctx, suspension, {
          kindCode: KIND.Unsupported,
          msg: "pid unavailable",
        });
        return;
      }

      runtime.resumeProcessPidOk(ctx, suspension, BigInt(pid));
    },

    "process-stop": async ({ runtime, ctx, suspension, request }) => {
      const processId = request.processId;
      const state = processes.get(processId);
      if (!state) {
        runtime.resumeProcessStopErr(ctx, suspension, invalidHandle());
        return;
      }

      try {
        // Best-effort: mimic JVM `destroy()` which does not guarantee termination.
        state.child.kill();
        runtime.resumeProcessStopOk(ctx, suspension);
      } catch (e) {
        runtime.resumeProcessStopErr(ctx, suspension, processIoErrorFromNodeError(e));
      }
    },

    "process-wait-for": async ({ runtime, ctx, suspension, request }) => {
      const processId = request.processId;
      const state = processes.get(processId);
      if (!state) {
        runtime.resumeProcessWaitForErr(ctx, suspension, invalidHandle());
        return;
      }

      try {
        const { code, signal } = await state.exitPromise;
        runtime.resumeProcessWaitForOk(ctx, suspension, normalizeExitCode(code, signal));
      } catch (e) {
        runtime.resumeProcessWaitForErr(ctx, suspension, processIoErrorFromNodeError(e));
      }
    },

    "process-wait-for-timeout": async ({ runtime, ctx, suspension, request }) => {
      const processId = request.processId;
      const state = processes.get(processId);
      if (!state) {
        runtime.resumeProcessWaitForTimeoutErr(ctx, suspension, invalidHandle());
        return;
      }

      const ms = clampMs(bigintToSafeNumber(request.timeoutMs));
      if (state.exited) {
        runtime.resumeProcessWaitForTimeoutOk(ctx, suspension, true);
        return;
      }
      if (ms <= 0) {
        runtime.resumeProcessWaitForTimeoutOk(ctx, suspension, false);
        return;
      }

      let finished = false;
      try {
        await Promise.race([
          state.exitPromise.then(() => {
            finished = true;
          }),
          new Promise((resolve) => setTimeout(resolve, ms)),
        ]);
        runtime.resumeProcessWaitForTimeoutOk(ctx, suspension, finished);
      } catch (e) {
        runtime.resumeProcessWaitForTimeoutErr(ctx, suspension, processIoErrorFromNodeError(e));
      }
    },

    "process-stdin-write": async ({ runtime, ctx, suspension, request }) => {
      const processId = request.processId;
      const state = processes.get(processId);
      if (!state) {
        runtime.resumeProcessStdinWriteErr(ctx, suspension, invalidHandle());
        return;
      }

      try {
        await writeStdin(state, request.bytes);
        runtime.resumeProcessStdinWriteOk(ctx, suspension);
      } catch (e) {
        runtime.resumeProcessStdinWriteErr(ctx, suspension, processIoErrorFromNodeError(e));
      }
    },

    "process-stdout-read": async ({ runtime, ctx, suspension, request }) => {
      const processId = request.processId;
      const state = processes.get(processId);
      if (!state) {
        runtime.resumeProcessStdoutReadErr(ctx, suspension, invalidHandle());
        return;
      }

      try {
        const buf = await readStream("stdout", state, request.maxBytes);
        runtime.resumeProcessStdoutReadOk(ctx, suspension, bufferToU8(buf));
      } catch (e) {
        runtime.resumeProcessStdoutReadErr(ctx, suspension, processIoErrorFromNodeError(e));
      }
    },

    "process-stderr-read": async ({ runtime, ctx, suspension, request }) => {
      const processId = request.processId;
      const state = processes.get(processId);
      if (!state) {
        runtime.resumeProcessStderrReadErr(ctx, suspension, invalidHandle());
        return;
      }

      try {
        const buf = await readStream("stderr", state, request.maxBytes);
        runtime.resumeProcessStderrReadOk(ctx, suspension, bufferToU8(buf));
      } catch (e) {
        runtime.resumeProcessStderrReadErr(ctx, suspension, processIoErrorFromNodeError(e));
      }
    },

    "process-release": async ({ runtime, ctx, suspension, request }) => {
      const processId = request.processId;
      unregisterProcess(processId);
      runtime.resumeProcessReleaseOk(ctx, suspension);
    },
  };
}
